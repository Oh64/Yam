def name(place, bot, number):
    tortue.up()
    #tortue.left(90)
    tortue.right(2*90)
    if place == 1 : tortue.goto(-225,200)
    else : tortue.goto(-150,200)
    tortue.forward(5)
    tortue.right(90)
    tortue.forward(15)
    tortue.right(90)
    tortue.forward(20)
    tortue.down()
    if bot == False : tmp = "Joueur N" + str(number)
    else : tmp = "Bot N" + str(number)
    tortue.write(tmp, font=("arial", 10, 'bold'))
    tortue.left(90)

